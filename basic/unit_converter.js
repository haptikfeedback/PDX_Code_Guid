/**
 * Converts inch to ...
 */
function convertInch(to, value){
    if (to === 'inch'){
        return value * 1
    } else if (to === 'feet'){
        return value * 0.0833333
    } else if (to === 'yard'){
        return value * 0.0277778
    } else if (to === 'mile'){
        return value * 0.0000157828
    } else if (to === 'kilometer'){
        return value * 0.0000254
    } else if (to === 'meter'){
        return value * 0.0254
    } else if (to === 'centimeter'){
        return value * 2.54
    } else if (to === 'millimeter'){
        return value * 25.4
    }
}
